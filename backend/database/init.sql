-- ============================================================
--  11. ORDENES DE TRABAJO - nucleo del sistema
-- ============================================================

CREATE TABLE ordenes_trabajo (
    id_orden             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_empresa           BIGINT          NOT NULL REFERENCES empresas(id_empresa) ON DELETE RESTRICT,
    id_cliente           BIGINT          NOT NULL,
    id_dispositivo       BIGINT          NOT NULL,
    id_tecnico           BIGINT,
    id_estado            INT             NOT NULL,
    numero_orden         VARCHAR(20)     NOT NULL,
    codigo_seguimiento   VARCHAR(20)     NOT NULL,
    problema_reportado   VARCHAR(500)    NOT NULL,
    fecha_ingreso        TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    fecha_entrega        TIMESTAMPTZ,
    fecha_cierre         TIMESTAMPTZ,
    observaciones        VARCHAR(300),
    estado_pago          VARCHAR(20)     NOT NULL DEFAULT 'NO_SOLICITADO'
        CHECK (estado_pago IN ('NO_SOLICITADO', 'PENDIENTE', 'EN_REVISION', 'PAGADO', 'RECHAZADO')),
    updated_at           TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    CONSTRAINT fk_orden_cliente_empresa
        FOREIGN KEY (id_empresa, id_cliente)
        REFERENCES clientes(id_empresa, id_cliente)
        ON DELETE RESTRICT,
    CONSTRAINT fk_orden_dispositivo_empresa
        FOREIGN KEY (id_empresa, id_dispositivo)
        REFERENCES dispositivos(id_empresa, id_dispositivo)
        ON DELETE RESTRICT,
    CONSTRAINT fk_orden_dispositivo_cliente
        FOREIGN KEY (id_cliente, id_dispositivo)
        REFERENCES dispositivos(id_cliente, id_dispositivo)
        ON DELETE RESTRICT,
    CONSTRAINT fk_orden_tecnico_empresa
        FOREIGN KEY (id_empresa, id_tecnico)
        REFERENCES tecnicos(id_empresa, id_tecnico)
        ON DELETE RESTRICT,
    CONSTRAINT fk_orden_estado_empresa
        FOREIGN KEY (id_empresa, id_estado)
        REFERENCES estados_reparacion(id_empresa, id_estado)
        ON DELETE RESTRICT,
    CONSTRAINT uq_orden_numero_empresa UNIQUE (id_empresa, numero_orden),
    CONSTRAINT uq_orden_codigo_seguimiento UNIQUE (codigo_seguimiento),
    CONSTRAINT uq_orden_empresa_id UNIQUE (id_empresa, id_orden)
);


