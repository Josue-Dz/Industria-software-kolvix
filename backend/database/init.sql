-- ============================================================
--  3. EMPRESA
-- ============================================================

CREATE TABLE empresas (
    id_empresa          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre              VARCHAR(100)    NOT NULL,
    rtn                 VARCHAR(20)     UNIQUE,
    telefono            VARCHAR(20),
    correo              VARCHAR(100)    NOT NULL UNIQUE,
    direccion           VARCHAR(255),
    fecha_registro      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    plan_suscripcion    VARCHAR(20)     NOT NULL DEFAULT 'BASICO'
        REFERENCES planes_suscripcion(codigo) ON DELETE RESTRICT,
    activo              BOOLEAN         NOT NULL DEFAULT TRUE,
    updated_at          TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);


-- ============================================================
--  4. CUENTAS DE PAGO DEL TALLER
-- ============================================================

CREATE TABLE cuentas_pago_taller (
    id_cuenta       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
