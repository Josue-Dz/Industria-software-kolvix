-- ============================================================
--  13. DIAGNOSTICOS
-- ============================================================

CREATE TABLE diagnosticos (
    id_diagnostico             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_empresa                 BIGINT          NOT NULL,
    id_orden                   BIGINT          NOT NULL,
    id_tecnico                 BIGINT          NOT NULL,
    problema_encontrado        TEXT            NOT NULL,
    causa_raiz                 TEXT,
    tiempo_estimado_horas      NUMERIC(5,2),
    complejidad                VARCHAR(20)
        CHECK (complejidad IS NULL OR complejidad IN ('BAJA', 'MEDIA', 'ALTA')),
    fecha_diagnostico          TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    observaciones_adicionales  TEXT,

    CONSTRAINT fk_diagnostico_orden_empresa
        FOREIGN KEY (id_empresa, id_orden)
        REFERENCES ordenes_trabajo(id_empresa, id_orden)
        ON DELETE CASCADE,
    CONSTRAINT fk_diagnostico_tecnico_empresa
        FOREIGN KEY (id_empresa, id_tecnico)
        REFERENCES tecnicos(id_empresa, id_tecnico)
        ON DELETE RESTRICT,
    CONSTRAINT uq_diagnostico_empresa_id UNIQUE (id_empresa, id_diagnostico),
    CONSTRAINT ck_diagnostico_tiempo_no_negativo CHECK (
        tiempo_estimado_horas IS NULL OR tiempo_estimado_horas >= 0
    )
);


-- ============================================================
--  14. COTIZACIONES - fase clave del MVP
-- ============================================================

CREATE TABLE cotizaciones (
    id_cotizacion              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_empresa                 BIGINT          NOT NULL,
    id_orden                   BIGINT          NOT NULL,
    id_diagnostico             BIGINT          NOT NULL,
    id_usuario_creador         BIGINT          NOT NULL,
    version                    SMALLINT        NOT NULL DEFAULT 1,
    monto_mano_obra            NUMERIC(10,2)   NOT NULL DEFAULT 0,
    monto_repuestos            NUMERIC(10,2)   NOT NULL DEFAULT 0,
    monto_total                NUMERIC(10,2)   NOT NULL DEFAULT 0,
    tiempo_estimado_horas      NUMERIC(5,2),
    estado                     VARCHAR(20)     NOT NULL DEFAULT 'PENDIENTE'
        CHECK (estado IN ('PENDIENTE', 'ENVIADA', 'APROBADA', 'RECHAZADA', 'VENCIDA', 'CANCELADA')),
    fecha_creacion             TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    fecha_envio                TIMESTAMPTZ,
    fecha_respuesta            TIMESTAMPTZ,
    observacion_cliente        VARCHAR(500),
    observacion_interna        VARCHAR(500),

    CONSTRAINT fk_cotizacion_orden_empresa
        FOREIGN KEY (id_empresa, id_orden)
        REFERENCES ordenes_trabajo(id_empresa, id_orden)
        ON DELETE CASCADE,
    CONSTRAINT fk_cotizacion_diagnostico
        FOREIGN KEY (id_empresa, id_diagnostico)
        REFERENCES diagnosticos(id_empresa, id_diagnostico)
        ON DELETE RESTRICT,
    CONSTRAINT fk_cotizacion_usuario_empresa
        FOREIGN KEY (id_empresa, id_usuario_creador)
        REFERENCES usuarios(id_empresa, id_usuario)
        ON DELETE RESTRICT,
    CONSTRAINT uq_cotizacion_version_diagnostico UNIQUE (id_diagnostico, version),
    CONSTRAINT uq_cotizacion_empresa_id UNIQUE (id_empresa, id_cotizacion),
    CONSTRAINT ck_cotizacion_montos_no_negativos CHECK (
        monto_mano_obra >= 0
        AND monto_repuestos >= 0
        AND monto_total >= 0
        AND (tiempo_estimado_horas IS NULL OR tiempo_estimado_horas >= 0)
    )
);


