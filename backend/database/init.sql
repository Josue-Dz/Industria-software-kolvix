-- ============================================================
--  TRIGGERS
--  Nota: updated_at y fecha_registro pueden manejarse desde Hibernate
--  con @UpdateTimestamp y @CreationTimestamp.
-- ============================================================

CREATE OR REPLACE FUNCTION fn_actualizar_calificacion_marketplace()
RETURNS TRIGGER AS $$
DECLARE
    v_id_empresa BIGINT;
BEGIN
    v_id_empresa := COALESCE(NEW.id_empresa, OLD.id_empresa);

    UPDATE perfiles_marketplace
    SET calificacion_promedio = (
            SELECT COALESCE(ROUND(AVG(calificacion)::NUMERIC, 2), 0)
            FROM reviews
            WHERE id_empresa = v_id_empresa
        ),
        total_reviews = (
            SELECT COUNT(*)
            FROM reviews
            WHERE id_empresa = v_id_empresa
        ),
        fecha_actualizacion = NOW()
    WHERE id_empresa = v_id_empresa;

    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_review_calificacion
    AFTER INSERT OR UPDATE OR DELETE ON reviews
    FOR EACH ROW EXECUTE FUNCTION fn_actualizar_calificacion_marketplace();


