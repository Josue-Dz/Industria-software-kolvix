    id_repuesto        BIGINT          NOT NULL REFERENCES repuestos(id_repuesto) ON DELETE RESTRICT,
    id_orden           BIGINT          REFERENCES ordenes_trabajo(id_orden) ON DELETE SET NULL,
    id_usuario         BIGINT          REFERENCES usuarios(id_usuario),
