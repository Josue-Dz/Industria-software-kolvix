        CHECK (tipo_movimiento IN ('ENTRADA', 'SALIDA', 'AJUSTE', 'DEVOLUCION')),
    cantidad           INT             NOT NULL CHECK (cantidad > 0),
    precio_unitario    NUMERIC(10,2)   NOT NULL DEFAULT 0 CHECK (precio_unitario >= 0),
