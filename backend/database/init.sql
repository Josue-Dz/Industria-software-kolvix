-- ============================================================
--  23. REPUESTOS REQUERIDOS EN DIAGNOSTICO
-- ============================================================

CREATE TABLE diagnosticos_repuestos (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_empresa          BIGINT          NOT NULL,
    id_diagnostico      BIGINT          NOT NULL,
    id_repuesto         BIGINT,
    nombre_repuesto     VARCHAR(100)    NOT NULL,
    cantidad            INT             NOT NULL CHECK (cantidad > 0),
    precio_unitario     NUMERIC(10,2)   NOT NULL CHECK (precio_unitario >= 0),
    subtotal            NUMERIC(10,2)   GENERATED ALWAYS AS (cantidad * precio_unitario) STORED,
    observacion         VARCHAR(255),

    CONSTRAINT fk_diagnostico_repuesto_diagnostico
        FOREIGN KEY (id_empresa, id_diagnostico)
        REFERENCES diagnosticos(id_empresa, id_diagnostico)
        ON DELETE CASCADE,
    CONSTRAINT fk_diagnostico_repuesto_repuesto
        FOREIGN KEY (id_empresa, id_repuesto)
        REFERENCES repuestos(id_empresa, id_repuesto)
        ON DELETE RESTRICT,
    CONSTRAINT ck_diagnostico_repuesto_referencia CHECK (
        id_repuesto IS NOT NULL OR LENGTH(TRIM(nombre_repuesto)) > 0
    )
);


