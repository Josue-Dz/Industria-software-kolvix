    banco           VARCHAR(100)    NOT NULL,
    tipo_cuenta     VARCHAR(20)     NOT NULL DEFAULT 'AHORRO'
        CHECK (tipo_cuenta IN ('AHORRO', 'CHEQUES', 'OTRO')),
    numero_cuenta   VARCHAR(50)     NOT NULL,
    titular         VARCHAR(120)    NOT NULL,
    moneda          VARCHAR(3)         NOT NULL DEFAULT 'HNL',
    instrucciones   VARCHAR(255),
    activo          BOOLEAN         NOT NULL DEFAULT TRUE,
    updated_at      TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_cuenta_pago_taller UNIQUE (id_empresa, banco, numero_cuenta),
    CONSTRAINT uq_cuenta_pago_taller_empresa_id UNIQUE (id_empresa, id_cuenta)
);


-- ============================================================
--  5. USUARIOS
-- ============================================================

CREATE TABLE usuarios (
    id_usuario              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_empresa              BIGINT          NOT NULL REFERENCES empresas(id_empresa) ON DELETE RESTRICT,
    nombre                  VARCHAR(50)     NOT NULL,
    apellido                VARCHAR(50)     NOT NULL,
    correo                  VARCHAR(100)    NOT NULL,
    password_hash           VARCHAR(255)    NOT NULL,
    rol                     VARCHAR(20)     NOT NULL DEFAULT 'RECEPCIONISTA'
        CHECK (rol IN ('ADMIN', 'TECNICO', 'RECEPCIONISTA', 'PROPIETARIO')),
    activo                  BOOLEAN         NOT NULL DEFAULT TRUE,
    debe_cambiar_password   BOOLEAN         NOT NULL DEFAULT FALSE,
    ultimo_acceso           TIMESTAMPTZ,
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_usuario_correo_empresa UNIQUE (id_empresa, correo),
    CONSTRAINT uq_usuario_empresa_id UNIQUE (id_empresa, id_usuario)
);


