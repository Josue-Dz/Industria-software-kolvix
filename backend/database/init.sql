-- ============================================================
--  2. CUENTAS DE COBRO
-- ============================================================

CREATE TABLE cuentas_cobro (
    id_cuenta       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    banco           VARCHAR(100)    NOT NULL,
    tipo_cuenta     VARCHAR(20)     NOT NULL DEFAULT 'AHORRO'
        CHECK (tipo_cuenta IN ('AHORRO', 'CHEQUES', 'OTRO')),
    numero_cuenta   VARCHAR(50)     NOT NULL,
    titular         VARCHAR(120)    NOT NULL,
    moneda          VARCHAR(3)         NOT NULL DEFAULT 'HNL',
    instrucciones   VARCHAR(255),
    activo          BOOLEAN         NOT NULL DEFAULT TRUE,
    updated_at      TIMESTAMPTZ     NOT NULL DEFAULT NOW()
